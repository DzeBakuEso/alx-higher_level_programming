#!/usr/bin/node
/**
 * Defines a class Rectangle.
 */
class Rectangle {
  /**
   * Constructor that initializes the rectangle's width and height.
   * @param {number} w - The width of the rectangle.
   * @param {number} h - The height of the rectangle.
   */
  constructor (w, h) {
    this.width = w;
    this.height = h;
  }
}

module.exports = Rectangle;
